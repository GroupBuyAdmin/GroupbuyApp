package groupbuyapp.Misc.Interface;

/**
 * The {@code Refreshable} interface represents an object that can be refreshed.
 * Objects that implement this interface provide a way to refresh their state or perform any other action when refreshed.
 * 
 * @author BSCS 2A Group 5
 */
public interface Refreshable {
    /**
     * Refreshes the object's state or performs any other action when refreshed.
     */
    public void refresh();
}
